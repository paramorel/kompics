package se.kth.ict.id2203.components.beb;

import se.kth.ict.id2203.ports.beb.BebDeliver;
import se.kth.ict.id2203.ports.pp2p.Pp2pDeliver;
import se.sics.kompics.address.Address;

class Message extends Pp2pDeliver {

	private static final long serialVersionUID = 7158574365728476025L;

	private final BebDeliver deliverEvent;

	protected Message(Address source, BebDeliver deliverEvent) {
		super(source);
		this.deliverEvent = deliverEvent;
	}

	public BebDeliver getDeliverEvent() {
		return deliverEvent;
	}
}
